'use strict';
const { expect } = require('chai');
const { LocalDateTime } = require('..');

describe('LocalDateTime', function () {
	it('is a subclass of Date', function () {
		expect(new LocalDateTime()).to.be.an.instanceof(Date);
		expect(Object.getPrototypeOf(new LocalDateTime())).to.equal(LocalDateTime.prototype);
		expect(Object.getPrototypeOf(LocalDateTime)).to.equal(Date);
		expect(LocalDateTime.prototype).to.not.equal(Date.prototype);
	});
	it('does not support static methods of Date', function () {
		expect(() => LocalDateTime.parse('2000-01-01')).to.throw(TypeError);
		expect(() => LocalDateTime.parse(new Date().toISOString())).to.throw(TypeError);
		expect(() => LocalDateTime.UTC(2000)).to.throw(TypeError);
		expect(() => LocalDateTime.now()).to.throw(TypeError);
	});
	describe('constructor', function () {
		it('uses the current time when no parameters are passed');
		it('interprets a Date argument as a UNIX timestamp');
		it('interprets a number argument as a UNIX timestamp');
		it('interprets a string argument as an ISO-formatted date-time string');
		it('does not support other argument types');
		it('does not support multiple constructor arguments');
	});
	describe('supported methods', function () {
		specify('valueOf()');
		specify('getTime()');
		specify('setTime()');
		specify('getMilliseconds()');
		specify('setMilliseconds()');
		specify('getUTCMilliseconds()');
		specify('setUTCMilliseconds()');
		specify('getSeconds()');
		specify('setSeconds()');
		specify('getUTCSeconds()');
		specify('setUTCSeconds()');
		specify('getMinutes()');
		specify('setMinutes()');
		specify('getUTCMinutes()');
		specify('setUTCMinutes()');
		specify('getHours()');
		specify('setHours()');
		specify('getUTCHours()');
		specify('setUTCHours()');
		specify('getDate()');
		specify('setDate()');
		specify('getUTCDate()');
		specify('setUTCDate()');
		specify('getMonth()');
		specify('setMonth()');
		specify('getUTCMonth()');
		specify('setUTCMonth()');
		specify('getYear()');
		specify('setYear()');
		specify('getFullYear()');
		specify('setFullYear()');
		specify('getUTCFullYear()');
		specify('setUTCFullYear()');
		specify('getDay()');
		specify('getUTCDay()');
		specify('getTimezoneOffset()');
		specify('toString()');
		specify('toUTCString()');
		specify('toGMTString()');
		specify('toISOString()');
		specify('toDateString()');
		specify('toTimeString()');
		specify('toLocaleDateString()');
		specify('toLocaleTimeString()');
		specify('toLocaleString()');
		specify('toJSON()');
	});
	describe('normalization', function () {
		specify('constructor');
		specify('setTime()');
		specify('setMilliseconds()');
		specify('setUTCMilliseconds()');
		specify('setSeconds()');
		specify('setUTCSeconds()');
		specify('setMinutes()');
		specify('setUTCMinutes()');
		specify('setHours()');
		specify('setUTCHours()');
		specify('setDate()');
		specify('setUTCDate()');
		specify('setMonth()');
		specify('setUTCMonth()');
		specify('setYear()');
		specify('setFullYear()');
		specify('setUTCFullYear()');
	});
	specify('getOriginalTimezoneOffset()');
	specify('setOriginalTimezoneOffset()');
});
